const { DataTypes, Sequelize } = require("sequelize");
const { connectionDb } = require("..");

const RecoveryPassword = connectionDb.define(
  "RecoveryPassword",
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true,
      autoIncrementIdentity: "GENERATED BY DEFAULT AS IDENTITY",
    },
    email: {
      type: DataTypes.STRING,
    },
    code: {
      type: DataTypes.STRING,
      field: "codigo",
    },
    isValidated: {
      type: DataTypes.BOOLEAN,
      field: "validado",
    },
    createdAt: {
      type: DataTypes.DATE,
      field: "criado_em",
      defaultValue: Sequelize.literal("CURRENT_TIMESTAMP"),
    },
    updatedAt: {
      type: DataTypes.DATE,
      field: "atualizado_em",
      defaultValue: Sequelize.literal("CURRENT_TIMESTAMP"),
    },
  },
  {
    tableName: "recuperacao_senha",
    timestamps: false,
  }
);

module.exports = { RecoveryPassword };
